'use strict';

describe('Controller Tests', function() {

    describe('Customer Management Detail Controller', function() {
        var $scope, $rootScope;
        var MockEntity, MockPreviousState, MockCustomer, MockSalesQuote, MockAddress, MockSalesOrder;
        var createController;

        beforeEach(inject(function($injector) {
            $rootScope = $injector.get('$rootScope');
            $scope = $rootScope.$new();
            MockEntity = jasmine.createSpy('MockEntity');
            MockPreviousState = jasmine.createSpy('MockPreviousState');
            MockCustomer = jasmine.createSpy('MockCustomer');
            MockSalesQuote = jasmine.createSpy('MockSalesQuote');
            MockAddress = jasmine.createSpy('MockAddress');
            MockSalesOrder = jasmine.createSpy('MockSalesOrder');
            

            var locals = {
                '$scope': $scope,
                '$rootScope': $rootScope,
                'entity': MockEntity,
                'previousState': MockPreviousState,
                'Customer': MockCustomer,
                'SalesQuote': MockSalesQuote,
                'Address': MockAddress,
                'SalesOrder': MockSalesOrder
            };
            createController = function() {
                $injector.get('$controller')("CustomerHelpopDetailController", locals);
            };
        }));


        describe('Root Scope Listening', function() {
            it('Unregisters root scope listener upon scope destruction', function() {
                var eventType = 'helpopApp:customerUpdate';

                createController();
                expect($rootScope.$$listenerCount[eventType]).toEqual(1);

                $scope.$destroy();
                expect($rootScope.$$listenerCount[eventType]).toBeUndefined();
            });
        });
    });

});
